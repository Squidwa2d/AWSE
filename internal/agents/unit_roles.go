// Package agents 的"单元化"版本 Dev/Review/Test.
//
// 与旧版一把梭的 Dev/Review/Test 不同, 这里每次只针对一个 Unit 执行:
//   - 产物写入 <ChangeDir>/units/<unit-id>/{dev,review,test}.md, 不覆盖旧单元
//   - prompt 里明确约束本轮只处理该 Unit 的 scope (软约束, 跨单元改动需在报告里声明)
package agents

import (
	"context"
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"time"

	"github.com/aswe/aswe/internal/adapter"
	"github.com/aswe/aswe/internal/state"
)

// UnitInput 针对单元的执行输入. 与 RunInput 区别在于多了 Module/Unit 上下文.
type UnitInput struct {
	ChangeID     string
	WorkspaceDir string
	ChangeDir    string
	ProjectDir   string

	Spec string // spec.md 全文 (给 AI 作上下文)
	Plan string // plan.md 全文 (给 AI 作上下文)

	Module *state.Module
	Unit   *state.Unit

	// Unit 自己的循环次数 (从 0 起, 第 N 次重跑时 = N)
	Iteration int

	// 上一轮的反馈: review 打回时 ReviewFeedback 非空; test 失败时 TestFeedback 非空.
	// Dev-Unit Agent 必须据此修复.
	ReviewFeedback string
	TestFeedback   string
}

// UnitOutput 单元级 Agent 的结果.
type UnitOutput struct {
	Passed     bool
	Adapter    string
	Summary    string
	OutputPath string
	Raw        string
}

// UnitAgent 单元粒度的角色接口.
type UnitAgent interface {
	Stage() state.Stage
	RunUnit(ctx context.Context, in *UnitInput) (*UnitOutput, error)
}

// --- 共享工具 ---

// unitDir 返回 <ChangeDir>/units/<unit-id>/, 不存在则创建.
func unitDir(changeDir, unitID string) (string, error) {
	safe := strings.ReplaceAll(unitID, string(os.PathSeparator), "_")
	dir := filepath.Join(changeDir, "units", safe)
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return "", err
	}
	return dir, nil
}

// invokeUnit 统一调用 CLI + 写产物.
func invokeUnit(ctx context.Context, cli adapter.CLIAdapter, stage state.Stage,
	in *UnitInput, prompt, workDir, fileName string, timeoutSec int,
) (*UnitOutput, error) {
	if workDir == "" {
		workDir = in.WorkspaceDir
	}
	if timeoutSec <= 0 {
		timeoutSec = 600
	}
	resp, err := cli.Invoke(ctx, adapter.Request{
		Prompt:         prompt,
		WorkDir:        workDir,
		Mode:           adapter.ModeHeadless,
		TimeoutSeconds: timeoutSec,
	})
	if err != nil {
		return nil, err
	}
	dir, err := unitDir(in.ChangeDir, in.Unit.ID)
	if err != nil {
		return nil, err
	}
	out := filepath.Join(dir, fileName)
	header := fmt.Sprintf("<!-- generated by aswe %s agent for unit %s iter=%d at %s via %s -->\n\n",
		stage, in.Unit.ID, in.Iteration, time.Now().Format(time.RFC3339), resp.Adapter)
	if err := os.WriteFile(out, []byte(header+resp.Output+"\n"), 0o644); err != nil {
		return nil, err
	}
	return &UnitOutput{
		Passed:     parseVerdict(resp.Output),
		Adapter:    resp.Adapter,
		Summary:    firstLines(resp.Output, 12),
		OutputPath: out,
		Raw:        resp.Output,
	}, nil
}

// ==================================================================
// Dev-Unit Agent
// ==================================================================

type DevUnitAgent struct{ cli adapter.CLIAdapter }

func NewDevUnit(cli adapter.CLIAdapter) *DevUnitAgent { return &DevUnitAgent{cli: cli} }
func (a *DevUnitAgent) Stage() state.Stage            { return state.StageDev }

func (a *DevUnitAgent) RunUnit(ctx context.Context, in *UnitInput) (*UnitOutput, error) {
	if in.ProjectDir == "" {
		return nil, fmt.Errorf("DevUnitAgent: ProjectDir 未配置")
	}
	if err := os.MkdirAll(in.ProjectDir, 0o755); err != nil {
		return nil, err
	}
	tree := listProjectTree(in.ProjectDir, 5)

	var feedback strings.Builder
	if strings.TrimSpace(in.ReviewFeedback) != "" {
		fmt.Fprintf(&feedback, "\n【上一轮 Code-Review 反馈, 必须逐条解决】\n----\n%s\n----\n", in.ReviewFeedback)
	}
	if strings.TrimSpace(in.TestFeedback) != "" {
		fmt.Fprintf(&feedback, "\n【上一轮 Test 失败信息, 必须修复】\n----\n%s\n----\n", in.TestFeedback)
	}

	iterBadge := ""
	if in.Iteration > 0 {
		iterBadge = fmt.Sprintf(" (第 %d 轮修复)", in.Iteration)
	}

	body := fmt.Sprintf(`你是 Dev-Agent, 本轮**只负责一个单元**%s.

== 当前单元 ==
模块: %s — %s
单元 ID: %s
标题: %s
负责范围 (scope): %s
交付物 (deliverable): %s

== 软约束 ==
- **优先只修改本单元 scope 内的文件**; 如确需跨单元改动(例如要修改公共入口/索引), 在
  dev.md 里用 "## 跨单元改动" 章节**显式列出文件并给理由**.
- 不要抢做其他单元的工作; 其他单元会由独立的 Dev 轮次处理.
- 新增依赖仍需写进项目级依赖清单, 禁止全局安装.

spec (需求真源):
----
%s
----

plan.md (已通过评审的整体方案, 仅供上下文, 不必重新实现整个方案):
----
%s
----
%s
当前项目目录已有文件:
----
%s
----

输出 (写入本单元的 dev.md):
- "## 实现摘要" : 本轮为单元 %s 做了什么.
- "## 变更文件清单" : "- path — 说明".
- "## 跨单元改动" : 没有就写"无".
- "## 如何本地验证" : 简述.
- "## 需要人类批准" : 按 NEEDS_HUMAN_APPROVAL: 开头列出要人批准的命令; 没有就写"无".
- 末尾单独一行: 完成且无阻塞 -> VERDICT: PASS; 否则 VERDICT: FAIL`,
		iterBadge, in.Module.ID, in.Module.Title,
		in.Unit.ID, in.Unit.Title, in.Unit.Scope, in.Unit.Deliverable,
		in.Spec, in.Plan, feedback.String(), tree, in.Unit.ID)

	prompt := withSafety(in.ProjectDir, safetyRulesForActor, body)
	return invokeUnit(ctx, a.cli, state.StageDev, in, prompt, in.ProjectDir, "dev.md", 1200)
}

