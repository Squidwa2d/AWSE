type DevUnitAgent struct{ cli adapter.CLIAdapter }

func NewDevUnit(cli adapter.CLIAdapter) *DevUnitAgent { return &DevUnitAgent{cli: cli} }
func (a *DevUnitAgent) Stage() state.Stage            { return state.StageDev }

func (a *DevUnitAgent) RunUnit(ctx context.Context, in *UnitInput) (*UnitOutput, error) {
	if in.ProjectDir == "" {
		return nil, fmt.Errorf("DevUnitAgent: ProjectDir 未配置")
	}
	if err := os.MkdirAll(in.ProjectDir, 0o755); err != nil {
		return nil, err
	}
	tree := listProjectTree(in.ProjectDir, 5)

	var feedback strings.Builder
	if strings.TrimSpace(in.ReviewFeedback) != "" {
		fmt.Fprintf(&feedback, "\n【上一轮 Code-Review 反馈, 必须逐条解决】\n----\n%s\n----\n", in.ReviewFeedback)
	}
	if strings.TrimSpace(in.TestFeedback) != "" {
		fmt.Fprintf(&feedback, "\n【上一轮 Test 失败信息, 必须修复】\n----\n%s\n----\n", in.TestFeedback)
	}

	iterBadge := ""
	if in.Iteration > 0 {
		iterBadge = fmt.Sprintf(" (第 %d 轮修复)", in.Iteration)
	}

	body := fmt.Sprintf(`你是 Dev-Agent, 本轮**只负责一个单元**%s.

== 当前单元 ==
模块: %s — %s
单元 ID: %s
标题: %s
负责范围 (scope): %s
交付物 (deliverable): %s

== 软约束 ==
- **优先只修改本单元 scope 内的文件**; 如确需跨单元改动(例如要修改公共入口/索引), 在
  dev.md 里用 "## 跨单元改动" 章节**显式列出文件并给理由**.
- 不要抢做其他单元的工作; 其他单元会由独立的 Dev 轮次处理.
- 新增依赖仍需写进项目级依赖清单, 禁止全局安装.

spec (需求真源, 仅作背景):
----
%s
----

plan.md (本单元相关章节, 完整方案见 %s/plan.md):
----
%s
----
%s
当前项目目录已有文件:
----
%s
----

输出 (**这份 dev.md 报告必须通过 stdout 返回**, 不要把它当成文件用 Write 写到磁盘 — aswe 会自行落盘; 但本单元的项目代码/测试文件应当照常用 Write 写入 ProjectDir):
- "## 实现摘要" : 本轮为单元 %s 做了什么.
- "## 变更文件清单" : "- path — 说明".
- "## 跨单元改动" : 没有就写"无".
- "## 如何本地验证" : 简述.
- "## 需要人类批准" : 按 NEEDS_HUMAN_APPROVAL: 开头列出要人批准的命令; 没有就写"无".
- 末尾单独一行: 完成且无阻塞 -> VERDICT: PASS; 否则 VERDICT: FAIL`,
		iterBadge, in.Module.ID, in.Module.Title,
		in.Unit.ID, in.Unit.Title, in.Unit.Scope, in.Unit.Deliverable,
		clipRunes(in.Spec, 3000),
		in.ArtifactDir, summarizePlanForUnit(in.Plan, in.Module.ID, 4000),
		feedback.String(), tree, in.Unit.ID)

	prompt := withSafety(in.ProjectDir, safetyRulesForActor, body)
	return invokeUnit(ctx, a.cli, state.StageDev, in, prompt, in.ProjectDir, "dev.md", 1200)
}

