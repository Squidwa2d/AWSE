type ReviewUnitAgent struct{ cli adapter.CLIAdapter }

func NewReviewUnit(cli adapter.CLIAdapter) *ReviewUnitAgent { return &ReviewUnitAgent{cli: cli} }
func (a *ReviewUnitAgent) Stage() state.Stage               { return state.StageReview }

func (a *ReviewUnitAgent) RunUnit(ctx context.Context, in *UnitInput) (*UnitOutput, error) {
	tree := listProjectTree(in.ProjectDir, 6)
	devReport := readUnitArtifact(in.ChangeDir, in.Unit.ID, "dev.md")

	body := fmt.Sprintf(`你是 Code-Review-Agent, 对**单元 %s** 的本轮 Dev 产出做审查.
只读不改. 结论仅覆盖本单元的 scope; 其他单元会由独立的 review 轮次审查.

模块: %s — %s
单元 scope: %s
单元交付物: %s

spec:
----
%s
----

plan.md:
----
%s
----

本单元 dev 报告:
----
%s
----

项目目录树:
----
%s
----

输出要求 (写入本单元 review.md):
- "## 结论" : 一句话是否通过.
- "## 对交付物的覆盖" : 交付物是否按承诺实现, 引用文件:行号或文件:函数.
- "## 对 scope 的符合度" : 是否主要改动在 scope 内; 跨单元改动是否合理.
- "## 问题清单" : 严重度(阻塞/建议), 文件位置, 修复建议.
- "## 给 Dev 的反馈" : 仅 FAIL 时填; 每条指向文件/函数并给出怎么改.
- 末尾单独一行:
	- 有阻塞级问题或交付物未实现 -> VERDICT: FAIL
	- 否则 -> VERDICT: PASS`,
		in.Unit.ID, in.Module.ID, in.Module.Title,
		in.Unit.Scope, in.Unit.Deliverable,
		in.Spec, in.Plan, devReport, tree)

	prompt := withSafety(in.ProjectDir, safetyRulesForReader, body)
	return invokeUnit(ctx, a.cli, state.StageReview, in, prompt, in.ProjectDir, "review.md", 600)
}

