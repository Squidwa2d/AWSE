type TestUnitAgent struct{ cli adapter.CLIAdapter }

func NewTestUnit(cli adapter.CLIAdapter) *TestUnitAgent { return &TestUnitAgent{cli: cli} }
func (a *TestUnitAgent) Stage() state.Stage             { return state.StageTest }

func (a *TestUnitAgent) RunUnit(ctx context.Context, in *UnitInput) (*UnitOutput, error) {
	tree := listProjectTree(in.ProjectDir, 6)
	devReport := readUnitArtifact(in.ChangeDir, in.Unit.ID, "dev.md")
	kind := detectProjectKind(in.ProjectDir)

	var strategy string
	timeoutSec := 900
	switch kind {
	case "go":
		strategy = `【项目类型: Go】仅用项目已有测试栈: "go test ./... -run <相关>". 无 _test.go 则就地新增 1-2 个覆盖本单元交付物.`
	case "node":
		strategy = `【项目类型: Node.js】优先已声明的 test script; 禁止新增测试框架; 可用 node:test + node:assert 写轻量测试覆盖本单元交付物.`
	case "python":
		strategy = `【项目类型: Python】只用已声明测试栈; 或用 unittest; 禁止 pip install 新包.`
	case "static":
		timeoutSec = 300
		strategy = `【项目类型: 静态前端】禁止 npm/pip/安装操作. 做: 1) 文件结构检查 2) node --check 语法检查 3) 对本单元涉及的纯函数抽取为 *.test.mjs 用 node:test 断言 4) 其他 spec 场景给"手工验证步骤".`
	default:
		timeoutSec = 300
		strategy = `【未识别类型】只做轻量静态自检: 列文件 + 语法检查 + 手工验证步骤.`
	}

	body := fmt.Sprintf(`你是 Test-Agent, 对**单元 %s** 的本轮交付做针对性验证.
只在 %s 内操作, 禁止 sudo/全局安装.

%s

模块: %s — %s
单元 scope: %s
单元交付物: %s

spec:
----
%s
----

本单元 dev 报告:
----
%s
----

项目目录树:
----
%s
----

输出 (写入本单元 test.md):
- "## 项目类型识别" : 一句话.
- "## 执行的验证命令" : 每条一行.
- "## 结果摘要" : 通过/失败/跳过 数量.
- "## 失败详情" : 失败时贴真实输出.
- "## 手工验证用例" : 静态/未识别类型填.
- "## 给 Dev 的反馈" : FAIL 时填.
- 末尾单独一行: 全通过 -> VERDICT: PASS; 硬失败 -> VERDICT: FAIL.`,
		in.Unit.ID, in.ProjectDir, strategy,
		in.Module.ID, in.Module.Title,
		in.Unit.Scope, in.Unit.Deliverable,
		in.Spec, devReport, tree)

	prompt := withSafety(in.ProjectDir, safetyRulesForActor, body)
	return invokeUnit(ctx, a.cli, state.StageTest, in, prompt, in.ProjectDir, "test.md", timeoutSec)
}

// readUnitArtifact 读取单元产物文件 (dev.md/review.md/test.md).
func readUnitArtifact(changeDir, unitID, name string) string {
	safe := strings.ReplaceAll(unitID, string(os.PathSeparator), "_")
	p := filepath.Join(changeDir, "units", safe, name)
	data, err := os.ReadFile(p)
	if err != nil {
		return ""
	}
	return string(data)
}
