type TestUnitAgent struct{ cli adapter.CLIAdapter }

func NewTestUnit(cli adapter.CLIAdapter) *TestUnitAgent { return &TestUnitAgent{cli: cli} }
func (a *TestUnitAgent) Stage() state.Stage             { return state.StageTest }

func (a *TestUnitAgent) RunUnit(ctx context.Context, in *UnitInput) (*UnitOutput, error) {
	tree := listProjectTree(in.ProjectDir, 6)
	devReport := readUnitArtifact(in.ArtifactDir, in.ChangeDir, in.Unit.ID, "dev.md")
	kind := detectProjectKind(in.ProjectDir)

	var strategy string
	timeoutSec := 900
	switch kind {
	case "go":
		strategy = `【项目类型: Go】仅用项目已有测试栈: "go test ./... -run <相关>". 无 _test.go 则就地新增 1-2 个覆盖本单元交付物.`
	case "node":
		strategy = `【项目类型: Node.js】优先已声明的 test script; 禁止新增测试框架; 可用 node:test + node:assert 写轻量测试覆盖本单元交付物.`
	case "python":
		strategy = `【项目类型: Python】只用已声明测试栈; 或用 unittest; 禁止 pip install 新包.`
	case "static":
		timeoutSec = 300
		strategy = `【项目类型: 静态前端】禁止 npm/pip/安装操作. 做: 1) 文件结构检查 2) node --check 语法检查 3) 对本单元涉及的纯函数抽取为 *.test.mjs 用 node:test 断言 4) 其他 spec 场景给"手工验证步骤".`
	default:
		timeoutSec = 300
		strategy = `【未识别类型】只做轻量静态自检: 列文件 + 语法检查 + 手工验证步骤.`
	}

	body := fmt.Sprintf(`你是 Test-Agent, 对**单元 %s** 的本轮交付做针对性验证.
只在 %s 内操作, 禁止 sudo/全局安装.

%s

模块: %s — %s
单元 scope: %s
单元交付物: %s

spec (背景, 已截断):
----
%s
----

本单元 dev 报告:
----
%s
----

项目目录树:
----
%s
----

输出 (**markdown 必须通过 stdout 返回**, 不要用 Write 落盘 test.md; 真要新增的测试文件本身则用 Write 写到 ProjectDir):
- "## 项目类型识别" : 一句话.
- "## 执行的验证命令" : 每条一行.
- "## 结果摘要" : 通过/失败/跳过 数量.
- "## 失败详情" : 失败时贴真实输出.
- "## 手工验证用例" : 静态/未识别类型填.
- "## 给 Dev 的反馈" : FAIL 时填.
- 末尾单独一行: 全通过 -> VERDICT: PASS; 硬失败 -> VERDICT: FAIL.`,
		in.Unit.ID, in.ProjectDir, strategy,
		in.Module.ID, in.Module.Title,
		in.Unit.Scope, in.Unit.Deliverable,
		clipRunes(in.Spec, 2000), devReport, tree)

	prompt := withSafety(in.ProjectDir, safetyRulesForActor, body)
	return invokeUnit(ctx, a.cli, state.StageTest, in, prompt, in.ProjectDir, "test.md", timeoutSec)
}

// readUnitArtifact 读取单元产物文件 (dev.md/review.md/test.md).
// 非法 unit id 返回空串, 不报错, 上层使用方会按"无反馈"处理.
func readUnitArtifact(artifactDir, legacyChangeDir, unitID, name string) string {
	safe, err := sanitizeUnitID(unitID)
	if err != nil {
		return ""
	}
	for _, dir := range []string{artifactDir, legacyChangeDir} {
		if dir == "" {
			continue
		}
		p := filepath.Join(dir, "units", safe, name)
		data, err := os.ReadFile(p)
		if err == nil {
			return string(data)
		}
	}
	return ""
}

// summarizePlanForUnit 为单元 prompt 生成 plan.md 的"瘦身版":
//   - 摘要前导段(plan.md 顶部到第一个 H2 之间), 仅取最多 maxRunes 字.
//   - 找到"## 模块 <id>"或包含 unit 所属 module ID 的 H2 章节, 截到下一个 H2/H1 为止.
//
// 这样 Dev/Review/Test 的 prompt 不再每轮都被 plan 全文淹没,
// 模型注意力会被自然地拉到与本单元相关的方案章节. 找不到 module 相关章节就退化为整篇.
func summarizePlanForUnit(plan, moduleID string, maxRunes int) string {
	if plan == "" {
		return ""
	}
	if maxRunes <= 0 {
		maxRunes = 4000
	}
	lines := strings.Split(plan, "\n")
	prefaceEnd := -1 // 第一个 H1/H2 出现的位置, 用作"前导段"边界
	moduleStart := -1
	for i, ln := range lines {
		t := strings.TrimSpace(ln)
		isHeading := strings.HasPrefix(t, "## ") || strings.HasPrefix(t, "# ")
		if isHeading && prefaceEnd < 0 {
			prefaceEnd = i
		}
		if strings.HasPrefix(t, "## ") {
			tit := strings.ToLower(strings.TrimPrefix(t, "## "))
			if strings.Contains(tit, strings.ToLower(moduleID)) {
				moduleStart = i
				break
			}
		}
	}
	var preface []string
	if prefaceEnd > 0 {
		preface = lines[:prefaceEnd]
	}
	if moduleStart < 0 {
		// 找不到对应模块章节, 退回原文; 由调用方决定是否再做整体截断.
		return plan
	}
	end := len(lines)
	for j := moduleStart + 1; j < len(lines); j++ {
		t := strings.TrimSpace(lines[j])
		if strings.HasPrefix(t, "## ") || strings.HasPrefix(t, "# ") {
			end = j
			break
		}
	}
	prefStr := strings.TrimSpace(strings.Join(preface, "\n"))
	prefStr = clipRunes(prefStr, maxRunes/2)
	modStr := strings.TrimSpace(strings.Join(lines[moduleStart:end], "\n"))
	modStr = clipRunes(modStr, maxRunes)
	if prefStr == "" {
		return modStr
	}
	return prefStr + "\n\n" + modStr
}

// clipRunes 按 rune 截断, 超出长度时追加省略提示.
func clipRunes(s string, n int) string {
	if n <= 0 {
		return ""
	}
	rs := []rune(s)
	if len(rs) <= n {
		return s
	}
	return string(rs[:n]) + "\n…(已截断)"
}

// ExtractFeedbackSection 从 review.md / test.md 这种 markdown 中, 提取"## 给 Dev 的反馈"
// 这种 H2 章节的纯文本(不含标题行), 截到下一个 H1/H2 或 "VERDICT:" 行为止.
// 这样下一轮 Dev/Unit 收到的 prompt 不会被整张报告(头注释/项目目录树/spec 全文等)淹没,
// 既省 token 也让模型的注意力聚焦在真正要修复的问题上.
//
// 找不到该 section 时退化为返回原文, 由调用方决定要不要额外裁剪.
func ExtractFeedbackSection(md, headerText string) string {
	if md == "" {
		return ""
	}
	lines := strings.Split(md, "\n")
	headerNorm := strings.ToLower(strings.TrimSpace(headerText))
	startIdx := -1
	for i, ln := range lines {
		t := strings.TrimSpace(ln)
		if strings.HasPrefix(t, "## ") {
			cur := strings.ToLower(strings.TrimSpace(strings.TrimPrefix(t, "## ")))
			if strings.Contains(cur, headerNorm) {
				startIdx = i + 1
				break
			}
		}
	}
	if startIdx < 0 {
		return strings.TrimSpace(md)
	}
	end := len(lines)
	for j := startIdx; j < len(lines); j++ {
		t := strings.TrimSpace(lines[j])
		if strings.HasPrefix(t, "## ") || strings.HasPrefix(t, "# ") || strings.HasPrefix(t, "VERDICT:") {
			end = j
			break
		}
	}
	return strings.TrimSpace(strings.Join(lines[startIdx:end], "\n"))
}
