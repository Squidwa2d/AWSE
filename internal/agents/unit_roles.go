// Package agents 的"单元化"版本 Dev/Review/Test.
//
// 与旧版一把梭的 Dev/Review/Test 不同, 这里每次只针对一个 Unit 执行:
//   - 产物写入 <ArtifactDir>/units/<unit-id>/{dev,review,test}.md, 不覆盖旧单元
//   - prompt 里明确约束本轮只处理该 Unit 的 scope (软约束, 跨单元改动需在报告里声明)
package agents

import (
	"context"
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"strings"
	"time"

	"github.com/aswe/aswe/internal/adapter"
	"github.com/aswe/aswe/internal/state"
)

// UnitInput 针对单元的执行输入. 与 RunInput 区别在于多了 Module/Unit 上下文.
type UnitInput struct {
	ChangeID     string
	WorkspaceDir string
	ChangeDir    string
	ArtifactDir  string
	ProjectDir   string

	Spec string // spec.md 全文 (给 AI 作上下文)
	Plan string // plan.md 全文 (给 AI 作上下文)

	Module *state.Module
	Unit   *state.Unit

	// Unit 自己的循环次数 (从 0 起, 第 N 次重跑时 = N)
	Iteration int

	// 上一轮的反馈: review 打回时 ReviewFeedback 非空; test 失败时 TestFeedback 非空.
	// Dev-Unit Agent 必须据此修复.
	ReviewFeedback string
	TestFeedback   string
}

// UnitOutput 单元级 Agent 的结果.
type UnitOutput struct {
	Passed     bool
	Adapter    string
	Summary    string
	OutputPath string
	Raw        string
}

// UnitAgent 单元粒度的角色接口.
type UnitAgent interface {
	Stage() state.Stage
	RunUnit(ctx context.Context, in *UnitInput) (*UnitOutput, error)
}

// --- 共享工具 ---

// unitIDRE 单元 ID 的合法字符集 (Plan-Agent 输出): 字母数字 + . _ -.
// 任何其它字符(空格/路径分隔符/中文/..) 都视为违例, 避免 AI 写出 "../etc" 之类
// 路径穿越 ID 把 dev.md 写到 ArtifactDir 之外.
var unitIDRE = regexp.MustCompile(`^[A-Za-z0-9._-]+$`)

// sanitizeUnitID 校验 ID 仅包含安全字符且没有 .. 等穿越前缀, 不合法则返回 error.
func sanitizeUnitID(unitID string) (string, error) {
	id := strings.TrimSpace(unitID)
	if id == "" {
		return "", fmt.Errorf("unit id 为空")
	}
	if !unitIDRE.MatchString(id) {
		return "", fmt.Errorf("unit id %q 含非法字符 (仅允许 [A-Za-z0-9._-])", unitID)
	}
	if id == "." || id == ".." || strings.Contains(id, "..") {
		return "", fmt.Errorf("unit id %q 含路径穿越片段", unitID)
	}
	return id, nil
}

// unitDir 返回 <ArtifactDir>/units/<unit-id>/, 不存在则创建.
func unitDir(artifactDir, unitID string) (string, error) {
	safe, err := sanitizeUnitID(unitID)
	if err != nil {
		return "", err
	}
	dir := filepath.Join(artifactDir, "units", safe)
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return "", err
	}
	return dir, nil
}

// invokeUnit 统一调用 CLI + 写产物.
func invokeUnit(ctx context.Context, cli adapter.CLIAdapter, stage state.Stage,
	in *UnitInput, prompt, workDir, fileName string, timeoutSec int,
) (*UnitOutput, error) {
	if workDir == "" {
		workDir = in.WorkspaceDir
	}
	if timeoutSec <= 0 {
		timeoutSec = 600
	}
	resp, err := cli.Invoke(ctx, adapter.Request{
		Prompt:         prompt,
		WorkDir:        workDir,
		Mode:           adapter.ModeHeadless,
		TimeoutSeconds: timeoutSec,
	})
	if err != nil {
		return nil, err
	}
	artifactDir := in.ArtifactDir
	if artifactDir == "" {
		artifactDir = in.ChangeDir
	}
	dir, err := unitDir(artifactDir, in.Unit.ID)
	if err != nil {
		return nil, err
	}
	out := filepath.Join(dir, fileName)
	header := fmt.Sprintf("<!-- generated by aswe %s agent for unit %s iter=%d at %s via %s -->\n\n",
		stage, in.Unit.ID, in.Iteration, time.Now().Format(time.RFC3339), resp.Adapter)
	if err := os.WriteFile(out, []byte(header+resp.Output+"\n"), 0o644); err != nil {
		return nil, err
	}
	return &UnitOutput{
		Passed:     parseVerdict(resp.Output),
		Adapter:    resp.Adapter,
		Summary:    firstLines(resp.Output, 12),
		OutputPath: out,
		Raw:        resp.Output,
	}, nil
}

